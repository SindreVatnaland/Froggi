<h1 class="text-2xl font-semibold">Create a new overlay</h1>

<h2>Lets start from scratch by creating a new overlay.</h2>

<h2>
	Click on the <b>OBS</b>
	icon on the right,
	<b>Custom Overlays</b>
	then
	<b>Add new</b>
</h2>

<h2>
	Here you will get an option to select <b>Horizontal</b>
	,
	<b>Vertical</b>
	or
	<b>Import</b>
	. The horizontal overlay is most commonly used for embedding in OBS while Vertical is mostly for
	mobile devices.
</h2>

<h2>
	It is possible to save and share overlays you have created with others. Therefore you also have
	the options to import others overlays. Be weary, only import overlays from trusted sources.
</h2>

<h2>
	For this tutorial, we will select
	<b>Horizontal</b>
