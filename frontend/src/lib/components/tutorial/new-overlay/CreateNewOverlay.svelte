<h1 class="text-2xl font-semibold">Create a new overlay</h1>

<h2>Lets start from scratch by creating a new overlay.</h2>

<h2>
	Click on the <b>OBS</b>
	icon on the right,
	<b>Overlays</b>
	then
	<b>Add new</b>
</h2>

<img
	class="border-secondary"
	src="/image/tutorial/new-overlay/find-overlays.png"
	alt="Navigate overlay page"
/>

<h2>
	Here you will get an option to select <b>Horizontal</b>
	,
	<b>Vertical</b>
	or
	<b>Import</b>
	. The horizontal overlays are commonly used for embedding in OBS, while the Vertical overlays are
	commonly used for mobile devices or vertical screens.
</h2>

<img
	class="border-secondary"
	src="/image/tutorial/new-overlay/create-new-overlay.png"
	alt="Create a new overlay"
/>

<h2>
	You can save and share overlays you have created with others. Thus there are an options to
	import others overlays. Be weary, only import overlays from trusted sources.
</h2>

<h2>
	For this tutorial, we will select
	<b>Horizontal</b>
</h2>

<h2>
	The new overlay will show up in the list of overlays with the name <b>
		New overlay - {'{overlay id}'}
	</b>
	.
</h2>

<img
	class="border-secondary"
	src="/image/tutorial/new-overlay/find-new-overlay.png"
	alt="Find new overlay"
/>

<h2>
	Click on the overlay and a preview of the overlay will appear. <br />
	Here you will be able to preview the overlay and its different scenes.
</h2>
<h2>
	Clicking on a scene will show all elements in that <b>scene</b>
	 with its appropriate transitions. Note that this preview will display any elements containing mock
	data that may otherwise be hidden in a live preview.
</h2>
<h2>
	Click the
	<b>Edit</b>
	button to start editing the overlay.
	<br />
	Click the
	<b>Duplicate</b>
	button to create a copy of the overlay.
	<br />
	Click the
	<b>Embed</b>
	button to get instructions to embed the overlay.
	<br />
	Click the
	<b>Save</b>
	button to save the overlay as a file on your computer which you can share with others.
	<br />
	Click the
	<b>Delete</b>
	button to delete the overlay.
</h2>

<img
	class="border-secondary"
	src="/image/tutorial/new-overlay/preview-overlay-popup.png"
	alt="Find new overlay"
/>

<h2>
	Click the
	<b>Edit</b>
	button to start editing the overlay.
</h2>

